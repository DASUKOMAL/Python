#Find the Simple Interest on Rs. 200 for 5 years at 5% per year.

# Print a message indicating the purpose of the program
print("Calculating simple interest")

# Define the amount, principal rate, and time period
Amount = 200   # Principal amount
Principal = 5  # Rate of interest
Time = 5      # Time period in years

# Calculate simple interest using the formula: SI = (Amount * Principal * Time) / 100
SI = (Amount * Principal * Time) / 100

# Print the calculated simple interest in a formatted string
print(f'The Simple Interest for {Amount} is {SI}')


#OUTPUT is 50.0
