# Program to demonstrate bitwise operators

x = 10  # 1010 in binary
y = 4   # 0100 in binary

print("x & y:", x & y)   # AND (0000)
print("x | y:", x | y)   # OR (1110)
print("x ^ y:", x ^ y)   # XOR (1110)
print("~x:", ~x)         # NOT (~1010)
print("x << 2:", x << 2) # Left shift (101000)
print("x >> 2:", x >> 2) # Right shift (0010)
